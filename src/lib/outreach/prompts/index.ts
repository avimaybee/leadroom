export function getChannelPrompt(channel: string): string {
  switch (channel) {
    case 'EMAIL': return EMAIL;
    case 'CALL': return CALL;
    case 'MEETING': return MEETING;
    case 'LINKEDIN':
    case 'SOCIAL': return SOCIAL;
    default: return EMAIL;
  }
}
